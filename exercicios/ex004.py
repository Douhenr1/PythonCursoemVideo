n = str(input("digite algo!"))
print ('O tipo primitivo é ', type(n))
print('só tem espaços', n.isspace ())
print('numerico', n.isnumeric ())
print('alfabeticos', n.isalpha ())
print('alfabético', n.isalnum())
print('está em maiúsculas', n.isupper ())
print('está em minúculas', n.islower())
print('está só com a primeira letra em maiúsulas', n.istitle())







